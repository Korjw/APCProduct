    login: true,
    logout: false,
    showModal: false,
    AlertData: [],
  }),
  methods: {
    signOut() {
      this.$axios
        .get("/users/api/logout", {})
        .then((res) => {
          alert(res.data.message);
          this.$store.commit("setUserId", "");
          this.$router.push("/users/login");
        })
        .catch((err) => {
          alert(err);
        });
    },
  },
  mounted() {
    this.AlertData = this.$store.getters.getAlertData;
  },
  computed: {
    checklogin: function () {
      return this.$store.getters.getUserId;
    },
    changeAlertData: function () {
      return this.$store.getters.getAlertData;
    },
  },
  watch: {
    checklogin(val) {
      if (val) {
        this.login = false;
        this.logout = true;
      } else {
        this.login = true;
        this.logout = false;
